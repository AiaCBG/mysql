-- Consulta todos (*) los usuarios de la tabla (users) ordenados (ORDER BY) por edad (age)
SELECT * FROM users ORDER BY age;

-- Consulta todos (*) los usuarios de la tabla (users) ordenados (ORDER BY) por edad (age) de manera Ascendente (ASC)
SELECT * FROM users ORDER BY age ASC;

-- Consulta todos (*) los usuarios de la tabla (users) ordenados (ORDER BY) por edad (age) de manera descendente (DESC)
SELECT * FROM users ORDER BY age DESC;