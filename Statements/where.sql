-- Consulta todos (*) los usuarios donde (WHERE) la edad (age) es igual a 15 (= 15)
SELECT * FROM users WHERE age = 15;

-- Consulta todos los nombres (name) donde (WHERE) la edad (age) es igual a 15 (= 15)
SELECT name FROM users WHERE age = 15;

-- Consulta todos la edad (age) distinta (DISTINCT) donde (WHERE) la edad (age) es igual a 15 (= 15)
SELECT DISTINCT age FROM users WHERE age = 15;